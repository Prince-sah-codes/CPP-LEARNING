#include<iostream>

using namespace std;

int main(){
    //WHILE LOOP 

    int a=1 ;
     while (a<=10){
        cout<< a<<endl;
        a++;
     }


    //FOR loop

    for(a=1; a<=10; a++){
        cout<< a<<endl;

    }

    //DO WHILE loop    
    do{
        cout<< a<<endl;
        a++;
    }while(a<=5);

    return 0;
}